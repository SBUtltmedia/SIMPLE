SIMPLE (pronounced "simple") allows you to take your favorite photo and transform it into an interactive online image map.
<br/><br/>
SIMPLE can be applied to a broad range of disciplines.  Go to <a href="?p=online_examples">Online Examples</a> to see how SIMPLE is already being integrated into the online learning environment.
<br/><br/>
If you're ready to start, go to <a href="?p=preparing_your_photo">Preparing Your Photo</a> to get your picture ready for the transformation.
<br/><br/>
After your photo is ready and in .psd form, you can go to the <a href="?p=simpl_uploader">SIMPLE Uploader</a> to make your image map!
